<tr><td>It is 6 years from first <a href="../../../pa1688/index.html">PA1688</a> chip ready to its end of life in 2006, and some of the most frequently asked questions were related with chip name and board name.
Someone would tell us, "My IP phone had a PA1688PQ chip inside it, please give me the update firmware". Some others would like to know how many chips we are selling instead. The repeated answer is, we have only one PA1688 chip,
based on the chip, there are different hardware reference designs like <a href="../../../pa1688/user/pa168s.html">PA168S</a> IP phone, <a href="../../../pa1688/user/pa168v.html">PA168V</a> 1-port FXS gateway.
The chip name is always PA1688, and PA168S, PA168V are board names. With the only PA1688, there are different firmware for different reference design board.
<br />Now it is the same with <a href="../../../ar1688/index.html">AR1688</a>. Based on the only one AR1688 chip, we are having different hardware reference designs and our customers will have different customized boards as well. The following are going to be available in the market in a short time, each has different upgrade firmware.
<br />1. AR168F standard IP phone reference design with 29 keys
<br />2. <a href="../../../ar1688/user/ywh201.html">YWH201</a> IP phone from Yuxin, mostly based on AR168F
<br />3. <a href="../../../ar1688/user/gf302.html">GF302</a> IP phone from High-link, mostly based on AR168F
<br />The PA1688 "no check" in "debug" option is no longer available in AR1688. With so many failure stories and support efforts on PA1688 in the past years, we now know it is a very bad idea to upgrade a device without any check, although it may help to debug in early stage development. Now in AR1688, upgrade files are all strictly checked before it is written to program flash without any exception.
