<tr><td><a href="../../../ar1688/index.html">AR1688</a> is a programable chip, we are using it for low cost IP phone solutions. 
It is the replacement chip for PA1688. The basic architectures are the same, both with 8-bit controller for VoIP protocol stacks and 24-bit DSP for voice processing and compression algorithm. 
Compared with PA1688, it has more powerful CPU processing speed and higher integration, the IP phone based on AR1688 has improved performance, better voice quality, more features, and the BOM is more simple, cost is lower too. 
Like PA1688, we will continue to provide full source code of VoIP protocols and user interface in our software API for third party development.
<br />Some details of the chip and solution:
<br />1. Controller speed at 48MHz (<a href="../../../pa1688/index.html">PA1688</a> at 22MHz)
<br />2. DSP at 60MIPS (PA1688 at 33MIPS)
<br />3. Integrated high quality audio codec (PA1688 need external codec, WM9707)
<br />4. Integrated large SRAM, no external SDRAM needed (PA1688 need 1x16 external SDRAM)
<br />5. RoHS ready
<br />6. ICMP response of 1472 bytes (ping xxx.xxx.xxx.xxx -l 1472): 10ms (PA1688 21ms)
<br />7. TFTP server data put through (tftp -i xxx.xxx.xxx.xxx put filename): 140kbytes/s
<br />8. MD5 48 bytes encryption calculation: 4.8ms (PA1688 9.9ms)
<br />9. Software API use open source SDCC compiler (PA1688 use commercial Keil C51)
