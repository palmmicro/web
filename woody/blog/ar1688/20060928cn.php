<tr><td><a href="../../../ar1688/indexcn.html">AR1688</a>是一个可编程芯片, 我们用它替代PA1688开发低成本的网络电话解决方案. 
二者的基本结构一致, 都是8位控制器用于VoIP协议栈, 24位DSP用于语音压缩算法. 同PA1688相比, AR1688具有更高的性能, 更好的语音质量, 更多的特点和更简单的BOM, 成本也更低. 
跟PA1688一致, 我们继续在AR1688上提供包括全部VoIP协议源码和用户界面代码的软件API供第三方开发. 
<br />具体的芯片和解决方案的细节: 
<br />1. 控制器运行在48MHz（<a href="../../../pa1688/indexcn.html">PA1688</a>跑22MHz）
<br />2. DSP运算速度60MIPS（PA1688是33MIPS）
<br />3. 集成的高性能模拟音频模块（PA1688需要额外的模拟音频AD/DA转换器如WM9707）
<br />4. 集成的大容量SRAM, 不需要额外的SDRAM（PA1688需要额外的1x16 SDRAM）
<br />5. 符合RoHS要求
<br />6. 1472字节的ICMP响应(ping xxx.xxx.xxx.xxx -l 1472): 10毫秒（PA1688 21毫秒）
<br />7. TFTP服务器数据流量(tftp -i xxx.xxx.xxx.xxx put filename): 140kbytes/s
<br />8. MD5 48字节加密运算: 4.8毫秒（PA1688 9.9毫秒）
<br />9. 软件API使用开源的SDCC编译器（PA1688使用商业的Keil C51）
