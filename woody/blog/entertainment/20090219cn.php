<h1>从Palmmicro到CSR的十年</h1>
<p>2009年2月19日
<br />某客户问我们是否能在接下来十年中保证现在<a href="../../../ar1688/indexcn.html">AR1688</a>的供货. 这当然是不可能的, 十年对一个芯片的生命周期来说实在是太长了. 
要做一个计划用十年的产品, 只能在芯片停产前的<font color=blue>最后购买</font>中买下够接下来十年用的货. 
<br />说实话十年对一个人来说也是很长的时间. 回想自己的经历, 十年前我只会编Windows和<a href="../ar1688/20070603cn.php">Dos</a>下的程序. 
当时我被招进<a href="../palmmicro/20061123cn.php">王老板</a>开的老Palmmicro公司, 开始做嵌入式系统的软件. 
从1999年到现在, <a href="../palmmicro/20080326cn.php">Palmmicro</a>改名Centrality, Centrality被<a href="20070813cn.php">SiRF</a>收购, SiRF又被<a href="http://www.csr.com/" target=_blank>CSR</a>收购, 城头变换大王旗, 我居然变成了一家英国公司的股东. 
