<h3>林蓉榕和林九霞</h3>
<p>2014年12月26日
<br />我有个中学男同学叫谭戟, 刀枪剑戟, 看上去很一般的男名. 等我上了大学, 发现同宿舍一个同学有个频繁写信的中学女同学叫黄戟, 这名字就怪了.
后来才知道这属于群体性的时代烙印, 其实就来自我们中学语文必背的:
</p>
<blockquote><font color=gray>红旗卷起农奴戟, 黑手高悬霸主鞭.</font></blockquote>
<p>今天是中国的圣诞节, 微信上又读到了跟我和我妹妹的名字直接相关的这首诗:</p>
<blockquote><font color=gray>九嶷山上白云飞, 帝子乘风下翠微. 斑竹一枝千滴泪, 红霞万朵百重衣.
<br />洞庭波涌连天雪, 长岛人歌动地诗. 我欲因之梦寥廓, 芙蓉国里尽朝晖.</font></blockquote>
<p>直到今天我都偶尔要跟人解释, 我跟成都没有关系, 名字中的<b>蓉</b>指的是我的出生地湖南. 不能怪我父母那个年代只有毛诗可读, 毕竟一千多年前五代谭用之在《秋宿湘江遇雨》就写过了:</p>
<blockquote><font color=gray>秋风万里芙蓉国, 暮雨千家薜荔村.</font></blockquote>
