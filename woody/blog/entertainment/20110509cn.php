<h1>Google投放的广告</h1>
<p>2011年5月9日
<br />随着我博客中内容增多, 有时候我发现有必要搜索曾经写过什么题材和内容. 我记得大约一年多前看到过绿野一个摄影爱好者搞的莫非时光(moftime.com现在已经不能访问)网站上有个Google搜索框.
我找到了Google Site Search却放弃了, 因为它每年要收至少100美元的服务费.
<br />在我使用<a href="20101107cn.php">Google Analytics</a>和Google Webmaster的过去这一年中, 我经常会看到AdSense和AdWords的链接. 
但是直到上周我从北京到深圳的路上读了篇有关<a href="http://www.360buy.com/" target=_blank>京东商城</a>如何做网络推广的文章后, 才理解这2个服务的意思.
<br />随后我注册了AdSense服务, Google用了一周批准我的帐户. 现在我把Google的搜索框(它叫AdSense for Search)以及投放的广告(AdSense for Content)放到了我所有的博客上, 就像本页最下面显示的一样.
</p>

<h3>Adsense周年小结</h3>
<p>2012年5月5日
<br />在我第一年的Adsense运营中, 大家阅读了15398次我的网络日志, 其中点击了50次广告. 给我的google帐户贡献了14.97美元的收入. 由于google满100美元支付一次, 我有希望在2019年把钱拿到手.
<br />另外根据Analytics的统计, 同期有8437个人访问了12487次<a href="../palmmicro/20080326cn.php">Palmmicro</a>网页, 看了总共74210个页面.
<br /><img src=../photo/20120505.jpg alt="Adsense anniversary summary and analytics statistics" />
</p>

<h3>Google的搜索框</h3>
<p>2015年10月4日
<br />由于<a href="../palmmicro/20100427cn.php">GFW</a>的影响, 从国内访问Palmmicro网站的时候Google的搜索框会导致所有Javascript延迟超过1分钟执行.
另外, 根据Adsense的统计, 在过去4年半这个搜索框一共也就被用过几十次, 估计都是我自己用的. 为改善用户体验, 我把它从其它页面都去掉了, 仅在本篇的中英文页面保留. 
</p>
